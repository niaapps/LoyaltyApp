package com.niaapplications.loyaltyapp;

public class Business {


 private String name, email, pw, street, state, city,zip, number;


    public Business(){
        this.name = name;
        this.email = email;
        this.number = number;
        this.city = city;
        this.pw = pw;
        this.street = street;
        this.state = state;
        this.zip = zip;
    }

    public Business(String name, String email, String pw, String street, String state, String city, String zip, String number){

        this.name = name;
        this.email = email;
        this.number = number;
        this.city = city;
        this.pw = pw;
        this.street = street;
        this.state = state;
        this.zip = zip;



    }
}

